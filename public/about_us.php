 <p>
 Established in the year 1985, Bharatiya Jain Sanghatana (BJS) has been in the forefront of addressing national concerns in the areas of Disaster Response, Social Development and Educational Initiatives. Having its head office in Pune, BJS is a non-political, non-profit, professionally managed Non-Governmental Organization (NGO) with a nationwide footprint working towards the benefit of all.
</p>
<p>
Over the years of its existence, BJS has practiced and perfected its approach and processes in the chosen areas of its work. It has rich and multi-dimensional experience of working at the grassroots as well as contributing to policy-level thought processes and decision making. BJS has worked with Central and many State governments and NGOs to implement many of its projects.
</p>
<p>
BJS works in progressional steps to achieve optimum and lasting impact - identifying issues, researching the needs, developing solutions based on practical experiences, implementing them sizable pilot projects to validate scalability, after analyzing the pilot results, and taking the modules countrywide roll-out and implementation.
</p><p>
The foundation of Bharatiya Jain Sanghatana is the Volunteers’ Network that is built very meticulously since day one. BJS has a nation-wide network of volunteers actively carrying out BJS responsibilities with relentless commitment. This volunteers’ network is the real strength of the organization. Having such a committed network till grass-root level serving for the social cause, BJS demonstrated the power of executing projects or many seemingly uphill tasks with speed and scale.
</p><p>
BJS has active volunteers’ network in Maharashtra, Chhattisgarh, Madhya Pradesh, Tamil Nadu, Karnataka, Uttar Pradesh, Andhra Pradesh, Gujarat, Rajasthan, Punjab, and Haryana. At many locations across all these 11 states and at few dispersed locations in other parts of India, BJS implements Social Development programs through its specially trained volunteers.
</p><p>
BJS has participated in Disaster Response activities in all major disasters since 1993 major earthquake of Latur - Osmanabad in Maharashtra. There onwards, BJS worked in Jabalpur (Madhya Pradesh) earthquake – 1996, Gujarat earthquake – 2001, Akola (Maharashtra) floods – 2002, Andaman & Nicobar tsunami – 2005, Jammu & Kashmir earthquake – 2005, Bihar floods – 2008, and Maharashtra drought – 2013 & 2016.
</p><p>
BJS focused on educational rehabilitation of disaster affected children since 1993 earthquake. From Latur & Osmanabad, BJS rehabilitated 1200 students and took complete responsibility of their education from standard 5th till their graduation. BJS established a permanent facility - Wagholi Educational Rehabilitation Center (WERC) near Pune in 1997. WERC has been hosting tribal students from Melghat (Amravati) and Kosbad (Thane) of Maharashtra since 1996. From November 2015, BJS took responsibility of rehabilitating children from suicide affected farmers’ families of Maharashtra. In 2016-17 academic year, 287 tribal boys and 653 boys and girls of farmers’ families are hosted at WERC for their education where all facility for their co-curricular development, sports and especially physical and mental health are provided with utmost care.
 </p>
